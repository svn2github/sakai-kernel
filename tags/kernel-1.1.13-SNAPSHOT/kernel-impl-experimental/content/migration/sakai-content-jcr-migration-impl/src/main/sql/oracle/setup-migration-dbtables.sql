  ON MIGRATE_CHS_CONTENT_TO_JCR(TIME_ADDED_TO_QUEUE)
/
CREATE INDEX MIGRATE_CHS_CONTENT_TO_JCR_EVENT_TYPE_INDEX
  ON MIGRATE_CHS_CONTENT_TO_JCR(EVENT_TYPE)
